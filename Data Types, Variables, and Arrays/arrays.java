
public class arrays {
    public static void main(String[] args) {
        
        // declare an array of integers
        // 'new' keyword is used to allocate memory for the array
        // we also specify the size of the array
        int nums[] = new int[3];
        // we can access array elements using their index
        nums[0] = 12;
        nums[1] = 44;
        nums[2] = 32;

        // declare an array and initialize the array elements
        int days[] = {1,3,4,7,12,17,19,22};
    }
}