
public class switch_statement {
    public static void main(String[] args) {
        
        int i = 2;

        switch (i) {
            case 1:
                System.out.println("Value of i is one");
                break;
            case 2:
                System.out.println("Value of i is two");
                break;
            case 3:
                System.out.println("Value of i is three");
                break;
            default:
                System.out.println("Value of i is invalid");
        }
    }
}