
public class classes {
    public static void main(String[] args) {
        
        // while loop
        int i = 5;
        while (i > 0) {
            System.out.println(i);
            i--;
        }

        // for loop
        for(int j=0; j<5; j++) {
            System.out.println(j);
        }
    }
} 